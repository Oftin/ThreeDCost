type TranslationKeys = typeof translations.pl;

export const i18n = (lang: Language) => {
  return translations[lang] as TranslationKeys;
};

export const useTranslation = () => {
  const { settings } = useContext(SettingsContext);
  if (!settings) {
    console.warn("SettingsContext not yet available in useTranslation. Using default English translations.");
    return translations.en as TranslationKeys;
  }
  return i18n(settings.language);
};
